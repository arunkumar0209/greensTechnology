package org.datatypes;

public class DataTypes {
	
	public static void main(String[] args) {
		
		//byte
		byte age = 23;
		System.out.println("My Age is: "+age);
		
		//short
		short empID=5747;
		System.out.println("My Employee ID is: "+empID);
		
		//int
		int regNo = 981236547;
		System.out.println("My Register No is: "+regNo);
		
		//long
		long phnNo = 9876543210l;
		System.out.println("My Phone No is: "+phnNo);
		
		//float
		float sal = 65438.46f;
		System.out.println("My Salary is: "+sal);
		
		//double
		double avg = 68.25;
		System.out.println("My Average is: "+avg);
		
		//char
		char block = 'A';
		System.out.println("My Block is: "+block);
		
		//String
		String name ="Arun Kumar S";
		System.out.println("My Name is: "+name);
		
		//boolean
		boolean isEmp = true;
		System.out.println("Is She/He is an Employee: "+isEmp);
		
	}

}
